class Factorial{
      public static void main (String[] args ) {
		   System.out.println("main started");
	 int   factNumber = 5;
	 int dammy =1;
	 for(int nik=1; nik<=factNumber; nik++) {
		dammy = dammy*nik ; 
	 }
	  System.out.println("The factorial of number is "+   factNumber  +  dammy);
	 System.out.println("main ended");
	  }
}