public class Patient
{
   private int id;
   private String name;
   private String hospitalName;
   private String consultantDoctor;
   private String diseaseName;
   private int wardNo;
   private String admittedDate;
   private String attenderName;
   private String address;
   private String gender;
   
   public void setId(int id){
	   this.id=id;
   }
   public int getId(){
	   return id;
   }
   public void setName(String name){
	   this.name=name;
   }
   public String getName(){
	   return name;
   }
   public void setHospitalName(String hospitalName){
	   this.hospitalName=hospitalName;
   }
   public String getHospitalName(){
	   return hospitalName;
   }
   public void setConsultantDoctor(String consultantDoctor){
	   this.consultantDoctor=consultantDoctor;
   }
   public String getConsultantDoctor(){
	   return consultantDoctor;
   }
   public void setDiseaseName(String diseaseName){
	   this.diseaseName=diseaseName;
   }
   public String getDiseaseName(){
	   return diseaseName;
   }
   public void setWardNo(int wardNo){
	   this.wardNo=wardNo;
   }
   public int getWardNo(){
	   return wardNo;
   }
   public void setAdmittedDate(String admittedDate){
	   this.admittedDate=admittedDate;
   }
   public String getAdmittedDate(){
	   return admittedDate;
   }
   public void setAttenderName(String attenderName){
	   this.attenderName=attenderName;
   }
   public String getAttenderName(){
	   return attenderName;
   }
   public void setAddress(String address){
	   this.address=address;
   }
   public String getAddress(){
	   return address;
   }
   public void setGender(String gender){
	   this.gender=gender;
   }
   public String getGender(){
	   return gender;
   }
}
   